/**
 * Variable paths may only reach as deep as the sender's STATIC outPort contract
 * (options[].value / schema.properties) actually declares. A deeper path — e.g.
 * `$.makeapicall.out.response.opportunityid` when MakeApiCall statically declares
 * just `response` / `status` / `statusText` — resolves fine at RUNTIME, but the
 * designer's variable picker cannot offer it, so the component renders a red
 * "invalid variable" chip (validation error) for the user.
 *
 * The fix is always the same: reference the deepest DECLARED path and extract the
 * leaf with a modifier —
 *   "variable": "$.<id>.out.response",
 *   "functions": [{ "name": "g_jsonPath", "params": [{ "value": "$.opportunityid" }] }]
 *
 * Ports with a dynamic `source` (options generated by a live describe call) are
 * skipped — their fields cannot be verified statically. Depth-1 references are
 * also skipped (covered by the structural / dynamic-output rules); this rule
 * targets exactly the "deep path under a static contract" class.
 */
import { collectVariables } from './lib/flowutil.js';
import { loadComponentSchema } from './lib/coverage.js';

export const name = 'static-outport-deep-path';
export const description = 'No variable paths deeper than the sender\'s static outPort options/schema — extract leaves with g_jsonPath';

const VAR_RE = /^\$\.([0-9a-fA-F-]{36})\.([A-Za-z0-9_-]+)\.(.+)$/;

// Does a JSON schema (of one option / property) statically declare `segments`?
const schemaDeclares = (schema, segments) => {
    let node = schema;
    for (const seg of segments) {
        const props = node?.properties;
        if (!props || !props[seg]) return false;
        node = props[seg];
    }
    return true;
};

export const run = (ctx) => {
    if (!ctx.connectorsDir) {
        for (const { file } of ctx.flows) {
            ctx.addWarning(file, '[static-outport-deep-path] connectorsDir not resolved — skipping');
            break;
        }
        return;
    }

    const cache = new Map();
    const defOf = (type) => {
        if (!cache.has(type)) cache.set(type, loadComponentSchema(type, ctx.connectorsDir));
        return cache.get(type);
    };

    for (const { file, json } of ctx.flows) {
        const flow = json.flow || {};
        const seen = new Set();

        for (const v of collectVariables(json)) {
            const m = VAR_RE.exec(v);
            if (!m || seen.has(v)) continue;
            const [, senderId, portName, rest] = m;
            const segments = rest.split('.');
            if (segments.length < 2) continue; // depth-1 handled by structural/dynamic-output rules

            const sender = flow[senderId];
            if (!sender) continue; // dangling sender — structural's job
            const def = defOf(sender.type);
            if (!def) continue; // component.json not resolvable

            const port = (def.outPorts || []).find((p) => (typeof p === 'string' ? p : p.name) === portName);
            if (!port || typeof port === 'string') continue;
            if (port.source) continue; // dynamic options — not statically verifiable

            const [head, ...deep] = segments;
            let declared = false;
            if (Array.isArray(port.options)) {
                const opt = port.options.find((o) => o.value === head);
                declared = !!opt && schemaDeclares(opt.schema, deep);
            } else if (port.schema) {
                declared = schemaDeclares(port.schema, segments);
            } else {
                continue; // no static contract at all — nothing to check against
            }

            if (!declared) {
                seen.add(v);
                const parent = `$.${senderId}.${portName}.${head}`;
                const jsonPath = `$.${deep.join('.')}`;
                ctx.addFailure(file,
                    `[static-outport-deep-path] variable "${v}" goes deeper than the static outPort ` +
                    `contract of ${sender.type} (port "${portName}") — the designer cannot offer it and ` +
                    `renders a red invalid-variable chip. Reference "${parent}" and extract the leaf with a ` +
                    `modifier: "functions": [{ "name": "g_jsonPath", "params": [{ "value": "${jsonPath}" }] }]`);
            }
        }
    }
};
